
# Note on ACS terminology and tables for what used to be called linguistic isolation


# The term "linguistically isolated"  (that was based on ACS Table B16004)
# was replaced by "limited English speaking household" (now based on ACS Table C16002).

# C16002 now has the info on "Households in which no one 14 and over speaks English only
# or speaks a language other than English at home and speaks English 'very well'."
# B16004 now has details on certain age groups but no cutpoint at age 14.

# Survey/Program:American Community Survey
# Universe:Households
# Year:2020
# Estimates:5-Year
# Table ID:C16002
# Source: U.S. Census Bureau, 2016-2020 American Community Survey 5-Year Estimates
#
# A "limited English speaking household" is one in which
# no member 14 years old and over
# (1) speaks only English or
# (2) speaks a non-English language and speaks English "very well."
# In other words,
# all members 14 years old and over have at least some difficulty with English.
#
# By definition, English-only households cannot belong to this group.
#
# ********
# Previous Census Bureau data products have referred to these households as
# "linguistically isolated" and "Households in which no one 14 and over speaks English only or
# speaks a language other than English at home and speaks English 'very well'."
# This table is directly comparable to tables from earlier years that used these labels.
# ********
#
# The household language assigned to the housing unit is the non-English
# language spoken by the first person with a non-English language in the following order:
#   reference person, spouse, parent, sibling, child, grandchild, in-law, other relative,
# unmarried partner, housemate/roommate, roomer/boarder, foster child, or other nonrelative.
# If no member of the household age 5 and over speaks a language other than English at home
# then the household language is English only.
